                    <strong class='text bold'>Red/Amber/Green Ratings</strong><br />
                    <p class='text'>The list has been reviewed by a wide range of local clinicians from a range of specialties to minimise errors and maximise its relevance for day to day practice. Each attachment in the clinical course has rated how likely you are to come across each <span class="bold">clinical problem</span> in that part of the course.</p><br />
