                    <strong class='text bold'>Introduction</strong><br />
                    <p class='text'>The ‘Clinical Problems and Conditions’ list has been developed as a resource to help students keep track of their clinical learning. It is an adaptation (with kind permission) of original material developed and published by Dr Henry Mandin. We are grateful for permission to use his earlier work.</p>
                </li>
