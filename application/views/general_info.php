        <article id="info2" class="list scroll intendeds">
            <ul>
                <li>
                    <strong class='text bold'>About the Problem List</strong><br />
                    <p class='text'>The list is composed of <span class='text bold'>‘Clinical problems’</span> with which patients may present. Each Clinical problem is associated with a list of possible <span class='text bold'>‘conditions’</span> that may cause it.</p><br />
                    <p class='text'>Conditions are further divided into <span class='text bold'>‘core conditions’</span> – those which are more common or important; and <span class='text bold'>'non-core conditions'</span>, which are less so, but nevertheless important to be aware of.</p><br />
                    <p class='text'>There are separate lists for Clinical problems and conditions for adults and children. It has recently been updated and is now (November 2012) ready for use.</p>
                </li>
                <li>
                    <strong class='text bold'>How to use the list</strong><br />
                    <p class='text'>We think there are 2 main ways to use the list, though you will doubtless find others:</p><br />
                    <p class='text'>1. As a tool to guide reading around problems seen, focusing more (though not exclusively) on the core conditions – ‘what should I read about having seen this clinical problem?’</p><br />
                    <p class='text'>2. As a  ‘map’ to review  how one’s learning is progressing – ‘which of these problems have I seen or learned about and which do  I need to address to cover the gaps?’</p>
                </li>
                <li>
                    <strong class='text bold'>Some comments about the list</strong>
                    <p class='text'>1. It is worth reviewing the full list of problems to develop an idea of how the list looks overall.</p><br />
                    <p class='text'>2. Some of the ‘Clinical problems’ are not really things that a patient would bring to a doctor, e.g. ‘abnormal biochemistry’. Some editorial licence has been needed to include categories such as these, but they follow the principle that such problems do come to a clinician’s attention and have associated conditions as their cause.</p><br />
                    <p class='text'>3. ‘Pain’ is difficult to categorise. We have taken a decision to label most pains by the location where they are experienced. A few are grouped together under ‘Regional pain’.</p><br />
                    <p class='text'>4. In a list of this length, there are bound to be inconsistencies. Please feel free to feed them back, but also to allow some editorial control. Please send any comments to: <a href="mailto:gpsiftadmin@medschl.cam.ac.uk">gpsiftadmin@medschl.cam.ac.uk</a>. We will take them into account when developing a future edition.</p>
                </li>
            </ul>
        </article>

