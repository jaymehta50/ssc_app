                </li>
                <li>
                    <p class='text'>Note that the colour rating applies to the Clinical Problem, not necessarily to all of the conditions associated with it. Currently, these ratings are estimates, but the rating can be used to help focus learning as you progress through the course. Note that Medicine and General Practice have rated the clinical problems relating to their attachments across the entire Clinical course. Each adult clinical problem is rated green or blue somewhere in the course. Some childrens’ clinical problems are not, reflecting the lower prevalence of disease amongst children.</p>
